package step1_01_print;

public class PrintEx02 {
	public static void main(String[] args) {
		// 자료형 (Data Type)
		
		// 1. 숫자 (정수) : 소수점이 없는 수
		System.out.println(10);
		// 2. 숫자 (실수) : 소수점이 있는 수
		System.out.println(3.14);
		// 3. 문자 한 개 : 싱글 따옴표
		System.out.println('W');
		// 4. 문자 여러개 : 더블 따옴표
		System.out.println("Winnie");
	}
}
